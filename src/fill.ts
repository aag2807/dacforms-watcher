import { Page, Locator, expect } from '@playwright/test';
import type { Locale } from '../config';

/**
 * A single fillable control discovered inside a form.
 */
export interface Field {
  locator: Locator;
  tag: 'input' | 'select' | 'textarea';
  type: string;          // input @type, '' for select/textarea
  name: string;
  id: string;
  placeholder: string;
  ariaLabel: string;
  required: boolean;
  autocomplete: string;
}

/** All searchable text hints for a field, lowercased. */
function hints(f: Field): string {
  return [f.name, f.id, f.placeholder, f.ariaLabel, f.autocomplete]
    .join(' ')
    .toLowerCase();
}

const matches = (h: string, ...words: string[]) => words.some((w) => h.includes(w));

